/** 构造用户 Prompt */
export function buildUserPrompt(
  description: string,
  price: number,
  city: string,
  imageCount: number,
  imageDescription: string | null,
): string {
  let imageSection: string;

  if (imageCount === 0) {
    imageSection = "用户未上传图片。缺少图片是重大信息缺失，请在报告中明确指出这一风险。";
  } else if (imageDescription) {
    // 视觉模型已识别图片内容
    imageSection = `用户上传了 ${imageCount} 张房源图片。以下是由视觉模型对图片内容的识别结果，你可以基于这些描述进行准确分析：

【图片内容描述】
${imageDescription}

请基于以上图片描述进行准确分析，包括：图片质量、是否效果图、装修水平、房间状况等。`;
  } else {
    // 有图片但无视觉模型识别结果
    imageSection = `用户上传了 ${imageCount} 张房源图片（视觉识别未启用，你无法直接查看这些图片）。请结合文字描述来判断房源情况。图片数量方面：${imageCount >= 5 ? "5张及以上，图片数量充足" : imageCount >= 3 ? "3-4张，基本够用" : "1-2张，偏少"}。如果用户在描述中说明了图片内容，请引用用户描述；如果用户没有说明，请在报告中标注"图片内容待确认"并给出基于文字信息的分析。`;
  }

  return `请分析以下房源信息，生成风险报告：

【房源文字描述】
${description || "（未提供任何文字描述，信息严重不足）"}

【月租金】${price} 元/月

【所在城市】${city}

【图片信息】${imageSection}

请根据你的分析框架，输出完整的 JSON 格式风险报告。`;
}
